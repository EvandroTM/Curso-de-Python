'''
Operadores de atribuição
= += -= *= /= //= **= %=
'''

contador = 0

contador = contador + 1
print(contador)

contador = 1

contador += 2
print(contador)

contador = '1'

contador += '2'
print(contador)

contador = 10

contador /= 2
print(contador)

contador = 10

contador //= 2
print(contador)

contador = 10


contador **= 2
print(contador)

contador = 10


contador %= 2
print(contador)