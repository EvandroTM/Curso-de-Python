'''
Repetições
while(enquanto)
executa uma ação enquanto uma condição for verdadeira
Loop inifinito → quando um código não tem fim.
'''

contador = 0

while contador <= 10: # contador conta de 0 a 10
    print(contador)
    contador = contador + 1

print('Fim')

